    const loadingContainer = document.querySelector('.loading-container');
    const loading = loadingContainer.querySelector('.loading');
    const progressText = loadingContainer.querySelector('.progress-text');

    let currentProgress = 0;
    let itv = setInterval(function() {
      if (currentProgress < 100) {
        const increment = Math.random() * 40;
        currentProgress += increment;
        if (currentProgress > 70) currentProgress = 70;
        setProgress(currentProgress);
      } else {
        clearInterval(itv);
      }
    }, 800);

    function setProgress(progress) {
      loading.style.width = `${progress}%`;
      progressText.textContent = `${Math.round(progress)}% das vagas preenchidas`;
    }
